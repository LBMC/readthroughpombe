  MAplotBetween2Cn(res, save.path.dir, paste("/MAplot_diff_expressed_", 
    save.dir, ".pdf", sep = ""), paste("nb diff. expressed features\nin classic analysis is ", 
    nb.diff.res, "(up=", length(which(res$padj < pthresh & res$log2FoldChange > 
      0)), " - down=", length(which(res$padj < pthresh & res$log2FoldChange < 
      0)), ")", sep = ""), pthresh, 0)
  
  MAplotBetween2Cn(resLFC, save.path.dir, paste("/MAplot_thresholdLFC_", 
    threshLFC, "_", save.dir, ".pdf", sep = ""), paste("nb diff. expressed features at LFC\nthreshold of ", 
    threshLFC, " is ", nb.diff.res.threshLFC, "(up=", dim(info.up.genes)[1], 
    " - down=", dim(info.down.genes)[1], ")", sep = ""), pthresh, c(-threshLFC, 
    threshLFC))
  
  ##### Implementation of test to compare densities/to assess presence of
  ##### clusters of DE genes
  if (do.perm.dist.tRNA) {
    # 10kb - 25kb - 50kb Calculate how many differentially expressed genes
    # had start position within 10kb of the start position of another
    # differentially expressed gene
    tmp.all <- rbind(info.up.genes[, c("V1", "deb")], info.down.genes[, 
      c("V1", "deb")], info.equ.genes[, c("V1", "deb")])
