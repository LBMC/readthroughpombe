    obs <- c()
    pval <- c()
    N <- 10000
    for (w in c(10000, 25000, 50000)) {
      print(w)
      dist.clust.obs <- list()
      for (chr in chr.diff.all) {
        temp <- tmp.all[tmp.all$V1 == chr, ]$deb
        tmp.diff <- rbind(info.up.genes[which(info.up.genes$V1 == 
          chr), "deb"], info.down.genes[which(info.down.genes$V1 == 
          chr), "deb"])$deb
