          tmp <- setdiff(tmp.diff, x)
          length(which(tmp >= (x - w) & tmp <= (x + w)))
        })
        dist.clust.obs <- c(dist.clust.obs, list(nb.clust))
        eval(parse(text = paste("temp.chr", chr, " = temp", sep = "")))
      }
      names(dist.clust.obs) <- chr.diff.all
      obs.w <- mean(unlist(dist.clust.obs))
      obs <- c(obs, obs.w)
      
      nb.diff.chrom <- lapply(dist.clust.obs, function(x) length(x))
      cl = parallel::makeCluster(parallel::detectCores())
      clusterExport(cl, c("nb.diff.chrom", "chr.diff.all", "w", "N", 
        paste("temp.chr", chr.diff.all, sep = "")))
      res = parSapply(cl, 1:N, function(x) {
        nb.clust.chr <- c()
        for (chr in chr.diff.all) {
          eval(parse(text = paste("temp <- temp.chr", chr, sep = "")))
          use <- temp[sample(seq_along(temp), nb.diff.chrom[[chr]], 
          replace = T)]
          nb.clust.chr <- c(nb.clust.chr, sapply(use, function(x) {
          tmp <- setdiff(use, x)
          length(which(tmp >= (x - w) & tmp <= (x + w)))
          }))
        }
        mean(nb.clust.chr)
      })
      pval <- c(pval, length(which(res > obs.w))/N)
      stopCluster(cl)
    }
    output_perm_density_feature <- data.frame(N = rep(N, length(pval)), 
      w = c("10kb", "25kb", "50kb"), pval = pval, obs.clust = obs, 
      stringsAsFactors = F)
    write.table(output_perm_density_feature, file = paste(save.path.dir, 
      "/test_clusters_DE_features_", save.dir, ".txt", sep = ""), 
      sep = "\t", col.names = T, row.names = F, quote = F)
  }
  
  ##### Density along chromosomes representation with sliding windows
  png(paste(save.path.dir, "/presence_along_chromosomes_thresholdLFC_", 
    threshLFC, "_", save.dir, ".png", sep = ""), w = 450 * length(chr.diff.all), 
    h = 750)
  w <- 10000
  o <- 1
  den.d <- NULL
  pos.d <- NULL
  den.nd <- NULL
  pos.nd <- NULL
  ud <- c()
  equ <- c()
  up <- c()
  dw <- c()
  for (chr in chr.diff.all) {
    print(chr)
    tmp.up <- info.up.genes[which(info.up.genes$V1 == chr), ]
    pos.genes.up <- unlist(apply(tmp.up, 1, function(x) x[3]:x[4]))
    tmp.down <- info.down.genes[which(info.down.genes$V1 == chr), ]
    pos.genes.down <- unlist(apply(tmp.down, 1, function(x) x[3]:x[4]))
    tmp.equ <- info.equ.genes[which(info.equ.genes$V1 == chr), ]
    pos.genes.not.diff <- unlist(apply(tmp.equ, 1, function(x) x[3]:x[4]))
    pos.genes.diff <- c(pos.genes.up, pos.genes.down)
    tab.diff <- ComputeWindowsOverChrom(vect.pos = pos.genes.diff, 
      size.chr = sizes[1, chr])
    TS <- zoo(tab.diff)
    roll.diff <- rollapply(TS, width = w, by = o, FUN = mean, align = "right")  #, partial = T)
    tab.not.diff <- ComputeWindowsOverChrom(vect.pos = pos.genes.not.diff, 
      size.chr = sizes[1, chr])
    TS <- zoo(tab.not.diff)
    roll.not.diff <- rollapply(TS, width = w, by = o, FUN = mean, align = "right")  #, partial = T)
    den.d <- c(den.d, roll.diff)
    pos.d <- c(pos.d, list(as.numeric(names(roll.diff))))
    den.nd <- c(den.nd, roll.not.diff)
    pos.nd <- c(pos.nd, list(as.numeric(names(roll.not.diff))))
    ud <- c(ud, dim(tmp.up)[1] + dim(tmp.down)[1])
    up <- c(up, dim(tmp.up)[1])
    dw <- c(dw, dim(tmp.down)[1])
    equ <- c(equ, dim(tmp.equ)[1])
  }
  offset <- sizes[1, chr.diff.all]
  offset <- c(0, cumsum(offset)[1:length(offset)])
  posd <- unlist(lapply(1:length(pos.d), function(x) pos.d[[x]] + offset[x]))
  posnd <- unlist(lapply(1:length(pos.nd), function(x) pos.nd[[x]] + 
    offset[x]))
  lim <- ceiling(max(c(den.d, den.nd)))
  plot(posd, den.d + lim, type = "p", pch = 16, cex = 0.7, main = paste("Presence of features - Chromosomes ", 
    paste(chr.diff.all, collapse = " "), " - ", save.dir, "\n window=", 
    w, "bp - pthresh=", pthresh, " - threshLFC = ", threshLFC, sep = ""), 
    xlab = "Position", ylab = paste("Count in window (size=", w, " bp) by overlap of ", 
      o, " bp", sep = ""), xlim = c(w, max(offset)), ylim = c(0, 
      2 * lim), yaxt = "n", col = "purple")
  axis(2, c(0, lim), c(0, lim), col = "black")
  axis(4, c(lim, 2 * lim), c(0, lim), col = "purple", col.ticks = "purple", 
    col.axis = "purple")
  points(posnd, den.nd, type = "p", col = 1, pch = 16, cex = 0.7)
  abline(h = 0, col = "gray")
  abline(h = lim, col = "gray")
  u <- par("usr")
  arrows(u[1], u[3], u[1], u[4], code = 2, xpd = TRUE)
  text(0.95 * offset[2:length(offset)], rep(1.85 * lim, length(offset) - 
    1), paste(ud, " up (", up, ")\n/downregulated (", dw, ")", sep = ""), 
    col = "darkorchid4", cex = 0.8)
  text(0.95 * offset[2:length(offset)], rep(lim * 0.85, length(offset) - 
    1), paste(equ, " equally\nexpressed", sep = ""), cex = 0.8, col = "darkgray")
  abline(v = offset, col = "darkgray", lwd = 1.5, lty = 2)
  dev.off()
  
  #### Distance between upregulated genes and the next tRNA promoters
  min.dist.up.tRNA <- sapply(1:dim(info.up.genes)[1], function(x) min(abs(gff.tRNA$deb[which(gff.tRNA$V1 == 
    info.up.genes$V1[x])] - info.up.genes$deb[x])))
  min.dist.down.tRNA <- sapply(1:dim(info.down.genes)[1], function(x) min(abs(gff.tRNA$deb[which(gff.tRNA$V1 == 
    info.down.genes$V1[x])] - info.down.genes$deb[x])))
  min.dist.up.down.tRNA <- c(min.dist.down.tRNA, min.dist.up.tRNA)
  min.dist.equal.tRNA <- sapply(1:dim(info.equ.genes)[1], function(x) min(abs(gff.tRNA$deb[which(gff.tRNA$V1 == 
    info.equ.genes$V1[x])] - info.equ.genes$deb[x])))
  
  ##### Fit NegBin GLM
  df.dist.tRNA <- data.frame(group = c(rep("equally.regulated", length(min.dist.equal.tRNA)), 
    rep("up.down.regulated", length(min.dist.up.down.tRNA))), distance.tRNA = c(min.dist.equal.tRNA, 
    min.dist.up.down.tRNA))
  nb.sample <- length(which(df.dist.tRNA$group == "up.down.regulated"))
  # subsample according to the nb of diff expressed genes
  df.tmp <- rbind(df.dist.tRNA[sample(which(df.dist.tRNA$group == "equally.regulated"), 
    nb.sample, replace = T), ], df.dist.tRNA[which(df.dist.tRNA$group == 
    "up.down.regulated"), ])
  # fit glm NB
  glm.nb.dist.tRNA <- glm.nb(distance.tRNA ~ group, data = df.tmp)
  pval.gr <- summary(glm.nb.dist.tRNA)[["coefficients"]][2, 4]
  
  pdf(paste(save.path.dir, "/dist_next_tRNA_thresholdLFC_", threshLFC, 
    "_", save.dir, ".pdf", sep = ""))
  ha <- hist(min.dist.equal.tRNA, breaks = 100, plot = F)
  hdiff <- hist(min.dist.up.down.tRNA, breaks = 100, plot = F)
  ylim <- c(0, max(c(hdiff$counts, ha$counts)))
  xlim <- c(0, max(c(hdiff$breaks, ha$breaks)))
  
  par(mar = c(5, 4, 4, 6) + 0.1)
  hist(min.dist.up.down.tRNA, breaks = 100, main = paste("Min distance between diff. expressed genes and tRNA promoters\nbreaks=100 - ", 
    save.dir, sep = ""), cex.main = 0.8, xlab = "Distance in bp", ylim = ylim, 
    xlim = xlim, col = adjustcolor("purple", 0.5))
  hist(min.dist.equal.tRNA, breaks = 100, add = T, col = adjustcolor("black", 
    0.25))
  
  mu.null <- exp(coefficients(glm.nb.dist.tRNA)[1])
  mu.1 <- exp(sum(coefficients(glm.nb.dist.tRNA)))
  xequ <- df.dist.tRNA[which(df.dist.tRNA$group == "equally.regulated"), 
    ]$distance.tRNA
  xequ <- xequ[order(xequ)]
  xdiff <- df.dist.tRNA[which(df.dist.tRNA$group == "up.down.regulated"), 
    ]$distance.tRNA
  xdiff <- xdiff[order(xdiff)]
  pred.null <- dnbinom(xequ, mu = mu.null, size = glm.nb.dist.tRNA$theta)
  pred.1 <- dnbinom(xdiff, mu = mu.1, size = glm.nb.dist.tRNA$theta)
  
  par(new = T)
  plot(xequ, pred.null, col = "black", lwd = 2, xlab = "", ylab = "", 
    type = "l", xaxt = "n", yaxt = "n")
  points(xdiff, pred.1, col = "purple", lwd = 2, xlab = "", ylab = "", 
    type = "l", xaxt = "n", yaxt = "n")
  axis(side = 4, ylim = c(0, 1))
  mtext(side = 4, line = 3, "Density")
  legend("topright", c("differentially expressed genes", "all but differentially expressed genes"), 
    col = c("purple", "darkgray"), lty = c(-1, -1), pch = c(15, 15), 
    bty = "n", cex = 0.6)
  if (pval.gr < 0.05) {
    txt <- paste("\nAt a risk of 5%, tRNA-gene distances are different in diff\nexpressed genes (pred=", 
      round(mu.1), " bp) compared to\nequally expressed genes (pred=", 
      round(mu.null), " bp)\n(pvalue, Wald test=", format(pval.gr, 
        scientific = T), ", GLM NegBin)", sep = "")
  } else {
    txt <- paste("\nAt a risk of 5%, we cannot say tRNA-gene distances are different in\ndiff expressed genes (pred=", 
      round(mu.1), " bp) compared to\nequally expressed genes(pred=", 
      round(mu.null), " bp)\n(pvalue, Wald test=", format(pval.gr, 
        scientific = T), ", GLM NegBin)", sep = "")
  }
  text(1.2 * xlim[2]/2, max(c(pred.null, pred.1))/2, txt, cex = 0.7)
  dev.off()
  
