##### parameters to set by user #####
pthresh <- 0.05
pref.date.counts <- "2017_09_27"
analysis <- list(c("cut14-208", "wt"), c("cut14-208_cdc15-118", "wt"), 
  c("cdc15-118", "wt"), c("cut14-208_cdc15-118", "cut14-208"), c("cut14-208_cdc15-118", 
    "cdc15-118"), c("rrp6D", "wt"), c("rrp6D", "cut14-208"))
ref.level <- c("wt", "wt", "wt", "cut14-208", "cdc15-118", "wt", "cut14-208")
do.perm.dist.tRNA <- F
threshLFC <- 0.5

##### Code #####

##### Read annotation file
annotation <- read.table("./data/2017_10_03_annotation.csv", sep = ";", 
  h = T, stringsAsFactors = F)  ##### annotation file 
gff <- fread("data/ReferenceGenomes/schizosaccharomyces_pombe.chr.gff3", 
  sep = "\t", h = F)
gff$deb <- apply(gff, 1, function(x) if (x[7] == "+") {
  x[4]
} else {
  x[5]
})
gff$deb <- as.numeric(gff$deb)

##### Pick features of interest
ind.transcripts <- which(gff$V2 == "PomBase" & gff$V3 %in% c("ncRNA_gene", 
  "pseudogene", "gene", "snoRNA_gene", "snRNA_gene") & gff$V1 %in% c("I", 
  "II", "III"))
gff.transcripts <- gff[ind.transcripts, ]
write.table(gff.transcripts[, -"deb"], "data/ReferenceGenomes/2017_10_03_selected_features_s_pombe.gff3", 
  sep = "\t", row.names = F, quote = F, col.names = F)

##### Pick tRNA
ind.tRNA <- which(gff$V2 == "PomBase" & gff$V3 == "tRNA_gene" & gff$V1 %in% 
  c("I", "II", "III"))
gff.tRNA <- gff[ind.tRNA, ]
write.table(gff.tRNA[, -"deb"], "data/ReferenceGenomes/2017_10_03_tRNAs_s_pombe.gff3", 
  sep = "\t", row.names = F, col.names = F, quote = F)

##### Id of feature to keep for differential analysis
keep.names <- unname(sapply(gff.transcripts$V9, function(x) strsplit(strsplit(x, 
  "ID=")[[1]][2], ";", fixed = T)[[1]][1]))

##### Sizes of chromosomes but MT
sizes <- matrix(c(5579133, 4539804, 2452883), nrow = 1, dimnames = list(NULL, 
  c("I", "II", "III")))

##### Size of features
size <- abs(gff.transcripts$V5 - gff.transcripts$V4)

##### Loop to perform each analysis
for (i in seq_along(analysis)) {
  analysis.tmp <- analysis[[i]]
  save.dir <- paste(analysis.tmp[1], "_vs_", analysis.tmp[2], sep = "")
  ##### Create one results dir per analysis
  system(paste("mkdir ./results/DESeq2_analysis/", save.dir, sep = ""))
  save.path.dir <- paste("./results/DESeq2_analysis/", save.dir, sep = "")
  ind.files <- which(annotation$strain %in% analysis.tmp)
  files.names.tmp <- annotation$file[ind.files]
  
  ##### Load quantification from RSEM
  files.tmp <- sapply(files.names.tmp, function(x) system(paste("ls ./results/DESeq2_analysis/counts/*_", 
    x, ".genes.results", sep = ""), intern = T))
  
  ##### Pick count data generated by RSEM:
  ##### https://bioconductor.org/packages/devel/bioc/vignettes/tximport/inst/doc/tximport.html
  names(files.tmp) <- annotation$sample[ind.files]
  txi.rsem <- tximport(files.tmp, type = "rsem")
  
  ##### Keep features for quantification
  ind.keep <- which(rownames(txi.rsem$counts) %in% keep.names)
  txi.rsem.keep <- c(lapply(txi.rsem[1:3], function(x) x[ind.keep, ]), 
    list(countsFromAbundance = txi.rsem$countsFromAbundance))
  
  sampleTable <- data.frame(condition = annotation$strain[ind.files])
  rownames(sampleTable) <- colnames(txi.rsem.keep$counts)
  
  ##### Check at log(txi.rsem$length) == 0 (i.e. log() on transcript/gene
  ##### lengths equal to 0) and remove the corresponding lines: reported in
  ##### https://support.bioconductor.org/p/82504/ check to remove effective
  ##### length
  ind.remove <- which(apply(txi.rsem.keep$length, 1, function(x) length(which(x == 
    0))) > 0)
  if (length(ind.remove) > 0) {
    txi.rsem.keep.notnull <- c(lapply(txi.rsem.keep[1:3], function(x) x[-ind.remove, 
      ]), list(countsFromAbundance = txi.rsem.keep$countsFromAbundance))
  } else {
    txi.rsem.keep.notnull <- txi.rsem.keep
  }
  
  rm(txi.rsem)  # remove non useful objects from R memory
  rm(txi.rsem.keep)
  
  ##### Input for DESeq analysis
  dds <- DESeqDataSetFromTximport(txi.rsem.keep.notnull, sampleTable, 
    ~condition)
  dds$condition <- relevel(dds$condition, ref = ref.level[i])
  
  ##### DESeq analysis: are there differentially expressed genes?
  dds <- DESeq(dds, betaPrior = T)
  res <- results(dds, addMLE = T)
  resLFC <- results(dds, lfcThreshold = threshLFC, altHypothesis = "greaterAbs", 
    addMLE = T)
  
  nb.diff.res <- length(which(res$padj < pthresh))
  nb.diff.res.threshLFC <- length(which(resLFC$padj < pthresh))
  
  pdf(paste(save.path.dir, "scale_thresholdLFC_signif_", save.dir, ".pdf", 
    sep = ""), width = 8, height = 4.5)
  par(mfrow = c(1, 2))
  plotMA(res, main = paste("nb diff. expressed features\n in classic analysis is ", 
    nb.diff.res, sep = ""), alpha = pthresh, ylim = c(-3, 3))
  abline(h = 0, col = "dodgerblue", lwd = 2)
  plotMA(resLFC, main = paste("nb diff. expressed features with LFC\nthreshold at ", 
    threshLFC, " is ", nb.diff.res.threshLFC, sep = ""), alpha = pthresh, 
    ylim = c(-3, 3))
  abline(h = c(-threshLFC, threshLFC), col = "dodgerblue", lwd = 2)
  dev.off()
  
  ##### Write results to txt files
  resLFC <- as.data.frame(resLFC[order(resLFC$padj), ])
  write.table(resLFC, file = paste(save.path.dir, "/deseq2_shrinked_analysis_thresholdLFC_", 
    threshLFC, "_", save.dir, ".txt", sep = ""), sep = "\t", col.names = T, 
    row.names = T, quote = F)  # classic DE analysis output (Wald test on log2(FC) to 0)
  res <- as.data.frame(res[order(res$padj), ])
  write.table(res, file = paste(save.path.dir, "/deseq2_shrinked_analysis_diff_expressed_", 
    save.dir, ".txt", sep = ""), sep = "\t", col.names = T, row.names = T, 
    quote = F)  # Wald test on log2(FC) to abs(threshLFC)
  
  ##### VolcanoPlot
  VolcanoPlot(resLFC, pthresh, save.path.dir, paste("/volcano_thresholdLFC_", 
    threshLFC, "_", save.dir, ".pdf", sep = ""), save.dir)
  
  ##### Pval hist
  PvalHist(resLFC, pthresh, save.path.dir, paste("/padj_hist_thresholdLFC_", 
    threshLFC, "_", save.dir, ".pdf", sep = ""), save.dir)
  # Note on p-values set to NA: some values in the results table can be
  # set to NA for one of the following reasons: If within a row, all
  # samples have zero counts, the baseMean column will be zero, and the
  # log2 fold change estimates, p value and adjusted p value will all be
  # set to NA. If a row contains a sample with an extreme count outlier
  # then the p value and adjusted p value will be set to NA. These
  # outlier counts are detected by Cookâ€™s distance.  If a row is filtered
  # by automatic independent filtering, for having a low mean normalized
  # count, then only the adjusted p value will be set to NA.
  
  ###### Equ/Up/downregulated genes along chromosomes
  for (pref in c("up", "down", "equ")) {
    ind <- which(resLFC$padj < pthresh & resLFC$log2FoldChange < 0)
    if (pref == "up") {
      ind <- which(resLFC$padj < pthresh & resLFC$log2FoldChange > 
        0)
    }
    if (pref == "equ") {
      ind <- which(resLFC$padj >= pthresh)
    }
    genes <- rownames(resLFC)[ind]
    l2fc <- sapply(genes, function(x) resLFC[which(rownames(resLFC) == 
      x), ]$log2FoldChange)
    info <- cbind(genes, gff.transcripts[sapply(genes, function(x) which(keep.names == 
      x)), c("V1", "V4", "V5", "V7", "deb")], l2fc)
    eval(parse(text = paste("info.", pref, ".genes <- info", sep = "")))
    eval(parse(text = paste("chrom.", pref, " <- unique(info$V1)", 
      sep = "")))
  }
  
  chr.diff.all <- unique(c(chrom.up, chrom.down))
  chr.diff.all <- chr.diff.all[order(chr.diff.all)]
  
