        threshLFC, "_", save.dir, ".pdf", sep = ""))
    
    cond <- unique(count.norm.comp[, c("samples", "chromosome")])
    mean.count.norm.comp <- do.call(rbind, sapply(as.character(unique(count.norm.comp$Var1)), 
      function(x) do.call(rbind, Filter(Negate(is.null), apply(cond, 
        1, function(y) {
          tmp <- which(count.norm.comp$samples == y[1] & count.norm.comp$chromosome == 
          y[2] & count.norm.comp$Var1 == x)
          if (length(tmp) > 0) {
          as.vector(c(x, y, mean(count.norm.comp[tmp, "value"])))
          }
        }))), simplify = F))
    colnames(mean.count.norm.comp) <- c("genes", "samples", "chromosome", 
      "value")
    mean.count.norm.comp <- as.data.frame(mean.count.norm.comp)
    mean.count.norm.comp$value <- as.numeric(as.character(mean.count.norm.comp$value))
    mean.count.norm.comp$genes <- as.character(mean.count.norm.comp$genes)
    ViolinPlot(input.df.with.count = mean.count.norm.comp, ylabel = "log2(mean normalized count + 1)", 
      save.pdf = paste(save.path.dir, "/mean_log2count_", type, "_expressed_thresholdLFC_", 
        threshLFC, "_", save.dir, ".pdf", sep = ""), T)
  }
  
  ##### Is there an enrichement in DE genes in some chromosomes?
  tab.equ <- table(info.equ.genes$V1)
  count.DE.nonDE.chr <- matrix(c(tab.equ, rep(0, length(tab.equ))), ncol = 3, 
    nrow = 2, byrow = T, dimnames = list(c("EE", "DE"), names(tab.equ)))
  tab.up <- table(info.up.genes$V1)
  count.DE.nonDE.chr[2, names(tab.up)] <- tab.up
  tab.d <- table(info.down.genes$V1)
  count.DE.nonDE.chr[2, names(tab.d)] <- count.DE.nonDE.chr[2, names(tab.d)] + 
    tab.d
  plot <- data.frame(chromosome = rep(colnames(count.DE.nonDE.chr), 2), 
    count = c(count.DE.nonDE.chr[1, ], count.DE.nonDE.chr[2, ]), type = c(rep(rownames(count.DE.nonDE.chr), 
      each = dim(count.DE.nonDE.chr)[2])))
  g.fisher <- ggplot(data = plot, aes(x = chromosome, y = count, fill = type)) + 
    geom_bar(stat = "identity", position = position_dodge()) + geom_text(aes(label = count), 
    vjust = 1.6, color = "black", position = position_dodge(0.9), size = 3.5) + 
    scale_fill_brewer(palette = "Paired") + theme_bw() + ggtitle(paste(save.dir, 
    "\nDE/EE genes per chromosome:\np.fisher = ", formatC(fisher.test(count.DE.nonDE.chr)$p.value, 
      digits = 3), sep = ""))
  
  pdf(paste(save.path.dir, "/DE_EE_per_chromosome_thresholdLFC_", threshLFC, 
    "_", save.dir, ".pdf", sep = ""))
  print(g.fisher)
